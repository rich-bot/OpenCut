export type EditorMessageKey = keyof typeof editorMessages;

export function editorT(
	key: EditorMessageKey,
	values: Record<string, string | number> = {},
): string {
	const message = editorMessages[key];
	return message.replace(/\{(\w+)\}/g, (match, valueKey) =>
		Object.prototype.hasOwnProperty.call(values, valueKey)
			? String(values[valueKey])
			: match,
	);
}

const DEFAULT_PROJECT_NAMES = new Set([
	"New project",
	"New Project",
	"Untitled project",
	"Untitled Project",
]);

export function localizeDefaultProjectName({ name }: { name: string }): string {
	return DEFAULT_PROJECT_NAMES.has(name.trim())
		? editorT("project.defaultName")
		: name;
}

const DEFAULT_SCENE_NAMES = new Map([
	["Main scene", editorT("scene.main")],
	["Main Scene", editorT("scene.main")],
]);

export function localizeDefaultSceneName({ name }: { name: string }): string {
	return DEFAULT_SCENE_NAMES.get(name.trim()) ?? name;
}
